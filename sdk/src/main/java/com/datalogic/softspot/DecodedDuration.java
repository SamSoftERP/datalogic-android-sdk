package com.datalogic.softspot;

/**
 * <code>DecodedDuration</code> is an enumeration defining constants for the
 * time periods SoftSpot can configured to display the decoded icon for.
 */
public enum DecodedDuration {
	/** 0 seconds */
	Sec0,
	/** 1 second */
	Sec1,
	/** 2 seconds */
	Sec2,
	/** 3 seconds */
	Sec3,
	/** 4 seconds */
	Sec4;
};