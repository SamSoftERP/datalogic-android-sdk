package com.datalogic.softspot;

/**
 * <code>ImageEnum</code> is an enumeration defining constants for different images that
 * can be displayed by SoftSpot.
 */
public enum ImageEnum {
	/** Green barcode (default) */
	GREEN_BARCODE,
	/** Red barcode (Shopevolution) */
	RED_TARGET,
	/** Bright green, barcode, with numeric data below */
	GREEN_BARCODE_NUMERIC;
};